using System.Threading.Tasks;
using Magicodes.Admin.Application.Authorization.Roles;
using Magicodes.Admin.Application.Authorization.Roles.Dto;
using Magicodes.Admin.Tests.Base;
using Shouldly;
using Xunit;

namespace Magicodes.Admin.Tests.Authorization.Roles
{
    public class RoleAppService_Tests : AppTestBase
    {
        public RoleAppService_Tests()
        {
            _roleAppService = Resolve<IRoleAppService>();
        }

        private readonly IRoleAppService _roleAppService;

        [MultiTenantFact]
        public async Task Should_Get_Roles_For_Host()
        {
            LoginAsHostAdmin();

            //Act
            var output = await _roleAppService.GetRoles(new GetRolesInput());

            //Assert
            output.Items.Count.ShouldBe(1);
        }

        [Fact]
        public async Task Should_Get_Roles_For_Tenant()
        {
            //Act
            var output = await _roleAppService.GetRoles(new GetRolesInput());

            //Assert
            output.Items.Count.ShouldBe(2);
        }
    }
}