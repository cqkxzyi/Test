using System;
using Abp.AspNetCore.Mvc.Controllers;
using Abp.IdentityFramework;
using Magicodes.Admin.Core;
using Magicodes.Admin.Localization;
using Microsoft.AspNetCore.Http;
using Microsoft.AspNetCore.Identity;

namespace Magicodes.Admin.Web.Host.Controllers
{
    public abstract class AdminControllerBase : AbpController
    {
        protected AdminControllerBase()
        {
            LocalizationSourceName = LocalizationConsts.LocalizationSourceName;
        }

        protected void CheckErrors(IdentityResult identityResult)
        {
            identityResult.CheckErrors(LocalizationManager);
        }

        protected void SetTenantIdCookie(int? tenantId)
        {
            Response.Cookies.Append(
                "Abp.TenantId",
                tenantId?.ToString(),
                new CookieOptions
                {
                    Expires = DateTimeOffset.Now.AddYears(5),
                    Path = "/"
                }
            );
        }
    }
}